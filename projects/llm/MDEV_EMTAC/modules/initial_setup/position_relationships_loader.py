    def load_equipment_relationships(self, file_path=None):
        """Main method to load equipment relationships data with ID-based mapping."""
        try:
            info_id("Equipment Relationships Data Import (ID-Based Mapping)")
            info_id("=" * 60)

            # Determine file path
            if not file_path:
                file_path = os.path.join(
                    DB_LOADSHEET, "Position_Loadsheet_Template.xlsx"
                )

            # Validate Excel file
            is_valid, message = self.validate_excel_file(file_path)
            if not is_valid:
                raise ValueError(f"Invalid Excel file: {message}")

            info_id(f"Loading from: {os.path.basename(file_path)}")

            # Get database session
            with self.db_config.main_session() as session:
                # Ensure tables exist
                self.create_database_tables(session)

                # Check existing data
                if not self.check_existing_data(session):
                    return False

                # Create backup
                self.create_database_backup(session)

                # Process each table in correct FK dependency order
                info_id("Processing tables with ID-based relationship mapping...")

                for sheet_name, model_class, required_columns in self.table_order:
                    try:
                        with log_timed_operation(
                                f"load_{sheet_name}_sheet", self.request_id
                        ):
                            df_raw = pd.read_excel(
                                file_path, sheet_name=sheet_name
                            )
                            df_cleaned = self.clean_dataframe(
                                df_raw, required_columns, sheet_name
                            )

                        self.process_single_table(
                            session, df_cleaned, model_class, sheet_name
                        )

                    except Exception as e:
                        error_id(
                            f"Error processing {sheet_name}: {str(e)}",
                            self.request_id,
                        )
                        self.stats["errors_encountered"] += 1
                        error_id(f"Error processing {sheet_name}: {str(e)}")
                        try:
                            session.rollback()
                        except Exception:
                            pass
                        continue

                # Remove duplicates (based on natural keys)
                # ✔ CRITICAL: Process in REVERSE dependency order to avoid FK violations
                info_id("Removing duplicates based on natural keys (not Excel IDs)...")
                info_id("Processing in reverse dependency order to handle foreign keys...")

                duplicate_mappings = [
                    # Children first (bottom of dependency tree)
                    (AssemblyView, "name", "AssemblyView"),
                    (ComponentAssembly, "name", "ComponentAssembly"),
                    (Subassembly, "name", "Subassembly"),
                    (Location, "name", "Location"),
                    # Then parents
                    (AssetNumber, "number", "AssetNumber"),
                    (Model, "name", "Model"),
                    (EquipmentGroup, "name", "EquipmentGroup"),
                    (Area, "name", "Area"),
                    (SiteLocation, "title", "SiteLocation"),
                    (Building, "name", "Building"),
                    (Campus, "name", "Campus"),
                ]

                for model_class, attribute, label in duplicate_mappings:
                    try:
                        self.delete_duplicates_enhanced(
                            session, model_class, attribute, label
                        )
                    except Exception as e:
                        # Log but continue - some tables might not have duplicates
                        warning_id(
                            f"Could not clean duplicates from {label}: {str(e)}",
                            self.request_id,
                        )
                        # Don't increment error count for expected FK issues
                        continue

                # Commit all changes
                session.commit()
                info_id(
                    "All database changes committed successfully",
                    self.request_id,
                )
                info_id("All changes committed to database")

                # Create revision snapshots (for subset of tables)
                self.create_revision_snapshots(session)

            # Display summary
            self.display_processing_summary()

            info_id("Equipment Relationships Import Completed Successfully!")
            info_id(
                "Equipment relationships import completed successfully",
                self.request_id,
            )

            return True

        except Exception as e:
            error_id(
                f"Equipment relationships import failed: {str(e)}",
                self.request_id,
                exc_info=True,
            )
            error_id(f"Import failed: {str(e)}")
            return False


