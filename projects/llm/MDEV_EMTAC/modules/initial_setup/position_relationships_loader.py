    # ------------------------------------------------------------------
    #  Helper: Resolve Excel FK to Database FK
    # ------------------------------------------------------------------
    def resolve_foreign_key(self, sheet_name, excel_id):
        """
        ✔ CRITICAL: Resolve Excel ID to database primary key.

        Args:
            sheet_name: The sheet/table name (e.g., "equipment_group")
            excel_id: The Excel ID from the foreign key column

        Returns:
            Database primary key (int) or None if not found
        """
        if excel_id is None:
            return None

        try:
            excel_id = int(excel_id)
        except (ValueError, TypeError):
            warning_id(
                f"Invalid Excel ID '{excel_id}' for {sheet_name}",
                self.request_id,
            )
            return None

        db_obj = self.excel_id_map.get(sheet_name, {}).get(excel_id)

        if db_obj is None:
            warning_id(
                f"Excel ID {excel_id} not found in {sheet_name} mapping",
                self.request_id,
            )
            return None

        # Return the actual database primary key
        return db_obj.id

