    def validate_excel_file(self, file_path):
        """Validate the Excel file structure and required sheets."""
        info_id(f"Validating Excel file: {file_path}", self.request_id)

        if not os.path.exists(file_path):
            error_id(f"Excel file not found: {file_path}", self.request_id)
            return False, "File does not exist"

        try:
            excel_file = pd.ExcelFile(file_path)

            required_sheets = [name for name, _, _ in self.table_order]
            missing_sheets = [
                sheet for sheet in required_sheets if sheet not in excel_file.sheet_names
            ]
            if missing_sheets:
                error_id(
                    f"Missing required sheets: {missing_sheets}", self.request_id
                )
                return False, f"Missing required sheets: {missing_sheets}"

            # Validate each sheet has at least headers
            for sheet_name in required_sheets:
                df = pd.read_excel(file_path, sheet_name=sheet_name)
                if df.empty:
                    warning_id(f"Sheet '{sheet_name}' is empty", self.request_id)

            info_id("Excel file validation successful", self.request_id)
            return True, "Valid"

        except Exception as e:
            error_id(f"Error validating Excel file: {str(e)}", self.request_id)
            return False, f"Error reading file: {str(e)}"

    def clean_dataframe(self, df, required_columns, sheet_name):
        """
        Clean and validate DataFrame with enhanced error handling.

        - Drops fully empty columns.
        - Ensures all required columns exist (but treats most as optional).
        - Returns only those columns in the expected order.
        - Drops rows where the primary "identity" column is empty (name / number / title).
        """
        info_id(f"Cleaning DataFrame for sheet: {sheet_name}", self.request_id)

        try:
            original_rows = len(df)

            # Remove completely empty columns
            df = df.dropna(axis=1, how="all")

            # Remove columns that are just empty strings
            for col in list(df.columns):
                if df[col].astype(str).str.strip().eq("").all():
                    df = df.drop(columns=[col])

            # Check if all required columns exist
            missing_columns = [col for col in required_columns if col not in df.columns]

            if missing_columns:
                # Critical columns that we absolutely need
                critical_columns = {"name", "number", "title"}
                critical_missing = [c for c in missing_columns if c in critical_columns]
                optional_missing = [c for c in missing_columns if c not in critical_columns]

                if critical_missing:
                    # ✔ IMPORTANT: Check if entire sheet is empty (no data rows)
                    # If sheet has no data, don't fail - just return empty dataframe
                    if len(df) == 0 or df.dropna(how='all').empty:
                        warning_id(
                            f"Sheet {sheet_name} is empty or has no data - skipping",
                            self.request_id,
                        )
                        # Return empty dataframe with required columns
                        return pd.DataFrame(columns=required_columns)

                    # Sheet has data but missing critical columns - this is an error
                    error_id(
                        f"Missing critical columns in {sheet_name}: {critical_missing}",
                        self.request_id,
                    )
                    raise ValueError(
                        f"Missing critical columns in {sheet_name}: {critical_missing}"
                    )

                # Non-critical columns → add them as None
                if optional_missing:
                    warning_id(
                        f"Missing optional columns in {sheet_name}: {optional_missing} "
                        f"- filling with None",
                        self.request_id,
                    )
                    for col in optional_missing:
                        df[col] = None

            # Replace NaN values with None for database compatibility
            df = df.replace({np.nan: None})

            # Return only required columns in the correct order
            cleaned_df = df[required_columns].copy()

            # Decide identity column for filtering out blank rows
            if "name" in required_columns:
                critical_fields = ["name"]
            elif "number" in required_columns:
                critical_fields = ["number"]
            elif "title" in required_columns:
                critical_fields = ["title"]
            else:
                critical_fields = []

            for field in critical_fields:
                initial_count = len(cleaned_df)
                cleaned_df = cleaned_df[
                    cleaned_df[field].notna()
                    & (cleaned_df[field].astype(str).str.strip() != "")
                    ]
                removed_count = initial_count - len(cleaned_df)
                if removed_count > 0:
                    warning_id(
                        f"Removed {removed_count} rows with empty {field} in {sheet_name}",
                        self.request_id,
                    )

            final_rows = len(cleaned_df)
            info_id(
                f"Cleaned {sheet_name}: {original_rows} -> {final_rows} rows",
                self.request_id,
            )

            return cleaned_df

        except Exception as e:
            error_id(
                f"Error cleaning DataFrame for {sheet_name}: {str(e)}",
                self.request_id,
            )
            raise

