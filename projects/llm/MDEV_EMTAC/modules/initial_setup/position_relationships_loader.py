    # ------------------------------------------------------------------
    #  FK-aware duplicate cleaners
    # ------------------------------------------------------------------
    def _dedupe_asset_numbers(self, session):
        """
        Merge duplicate AssetNumber rows that share the same 'number'.

        Strategy:
        - Treat AssetNumber.number as the natural key.
        - For each number with multiple rows:
            - Keep the lowest id row as canonical.
            - Re-point all FK references to the canonical id.
            - Delete the extra AssetNumber rows.
        This avoids setting any FK to NULL and keeps PostgreSQL happy.
        """
        label = "AssetNumber"
        info_id(
            f"FK-aware dedupe for {label} based on 'number'",
            self.request_id,
        )

        try:
            with session.no_autoflush:
                # Find all numbers that appear more than once
                duplicates = (
                    session.query(
                        AssetNumber.number,
                        func.count(AssetNumber.id).label("count"),
                    )
                    .group_by(AssetNumber.number)
                    .having(func.count(AssetNumber.id) > 1)
                    .all()
                )

                if not duplicates:
                    return

                duplicates_removed = 0

                for number_value, _ in duplicates:
                    rows = (
                        session.query(AssetNumber)
                        .filter(AssetNumber.number == number_value)
                        .order_by(AssetNumber.id.asc())
                        .all()
                    )

                    if len(rows) < 2:
                        continue

                    primary = rows[0]
                    extras = rows[1:]

                    debug_id(
                        f"[AssetNumber dedupe] number={number_value}, "
                        f"keeping id={primary.id}, merging {len(extras)} duplicates",
                        self.request_id,
                    )

                    for extra in extras:
                        # Re-point all children that reference this extra asset_number
                        # Location.asset_number_id
                        session.query(Location).filter(
                            Location.asset_number_id == extra.id
                        ).update(
                            {"asset_number_id": primary.id},
                            synchronize_session="fetch",
                        )

                        # Subassembly.asset_number_id
                        session.query(Subassembly).filter(
                            Subassembly.asset_number_id == extra.id
                        ).update(
                            {"asset_number_id": primary.id},
                            synchronize_session="fetch",
                        )

                        # ComponentAssembly.asset_number_id
                        session.query(ComponentAssembly).filter(
                            ComponentAssembly.asset_number_id == extra.id
                        ).update(
                            {"asset_number_id": primary.id},
                            synchronize_session="fetch",
                        )

                        # AssemblyView.asset_number_id
                        session.query(AssemblyView).filter(
                            AssemblyView.asset_number_id == extra.id
                        ).update(
                            {"asset_number_id": primary.id},
                            synchronize_session="fetch",
                        )

                        # Finally delete the extra AssetNumber row itself
                        session.delete(extra)
                        duplicates_removed += 1

                if duplicates_removed > 0:
                    info_id(
                        f"Removed {duplicates_removed} AssetNumber duplicates "
                        f"via FK-aware merge",
                        self.request_id,
                    )
                    self.stats["duplicates_removed"] += duplicates_removed

        except Exception as e:
            error_id(
                f"Error in FK-aware AssetNumber dedupe: {str(e)}",
                self.request_id,
            )
            raise

    def _dedupe_subassemblies(self, session):
        """
        Merge duplicate Subassembly rows safely.

        You chose **B**:
            - Subassembly.name is NOT globally unique.
            - The same name can exist on different assets.

        So the natural key is:
            (name, asset_number_id, location_id)

        Strategy:
        - For each (name, asset_number_id, location_id) that has >1 rows:
            - Keep the lowest id row as canonical.
            - Re-point ComponentAssembly.subassembly_id from the extras
              to the canonical id.
            - Delete the extra Subassembly rows.

        IMPORTANT:
        - We NEVER set subassembly_id to NULL.
        - We ONLY change it from one valid id to another valid id.
        """
        label = "Subassembly"
        info_id(
            f"FK-aware dedupe for {label} based on (name, asset_number_id, location_id)",
            self.request_id,
        )

        try:
            with session.no_autoflush:
                duplicates = (
                    session.query(
                        Subassembly.name,
                        Subassembly.asset_number_id,
                        Subassembly.location_id,
                        func.count(Subassembly.id).label("count"),
                    )
                    .group_by(
                        Subassembly.name,
                        Subassembly.asset_number_id,
                        Subassembly.location_id,
                    )
                    .having(func.count(Subassembly.id) > 1)
                    .all()
                )

                if not duplicates:
                    return

                duplicates_removed = 0

                for name_value, asset_number_id, location_id, _ in duplicates:
                    rows = (
                        session.query(Subassembly)
                        .filter(
                            Subassembly.name == name_value,
                            Subassembly.asset_number_id == asset_number_id,
                            Subassembly.location_id == location_id,
                        )
                        .order_by(Subassembly.id.asc())
                        .all()
                    )

                    if len(rows) < 2:
                        continue

                    primary = rows[0]
                    extras = rows[1:]

                    debug_id(
                        f"[Subassembly dedupe] name={name_value}, "
                        f"asset_number_id={asset_number_id}, location_id={location_id}, "
                        f"keeping id={primary.id}, merging {len(extras)} duplicates",
                        self.request_id,
                    )

                    for extra in extras:
                        # Re-point ComponentAssembly.subassembly_id
                        session.query(ComponentAssembly).filter(
                            ComponentAssembly.subassembly_id == extra.id
                        ).update(
                            {"subassembly_id": primary.id},
                            synchronize_session="fetch",
                        )

                        # Delete the extra subassembly
                        session.delete(extra)
                        duplicates_removed += 1

                if duplicates_removed > 0:
                    info_id(
                        f"Removed {duplicates_removed} Subassembly duplicates "
                        f"via FK-aware merge",
                        self.request_id,
                    )
                    self.stats["duplicates_removed"] += duplicates_removed

        except Exception as e:
            error_id(
                f"Error in FK-aware Subassembly dedupe: {str(e)}",
                self.request_id,
            )
            raise


    def delete_duplicates_enhanced(self, session, model, attribute, label):
        """
        Enhanced duplicate removal with detailed logging.

        Special cases:
        - AssetNumber: use FK-aware merge based on 'number'.
        - Subassembly: use FK-aware merge based on (name, asset_number_id, location_id).

        All other tables fall back to the generic:
        - group by the attribute
        - keep the first row
        - delete the extra rows
        """
        # FK-aware special cases first
        if model is AssetNumber:
            self._dedupe_asset_numbers(session)
            return

        if model is Subassembly:
            self._dedupe_subassemblies(session)
            return

        # Generic duplicate removal for the rest
        try:
            info_id(
                f"Removing duplicates from {label} based on '{attribute}'",
                self.request_id,
            )

            with session.no_autoflush:
                duplicates = (
                    session.query(
                        getattr(model, attribute),
                        func.count().label("count"),
                    )
                    .group_by(getattr(model, attribute))
                    .having(func.count() > 1)
                    .all()
                )

                duplicates_removed = 0

                for attr_value, _ in duplicates:
                    records = (
                        session.query(model)
                        .filter(getattr(model, attribute) == attr_value)
                        .order_by(model.id.asc())
                        .all()
                    )

                    # Keep the first record, delete the rest
                    for record in records[1:]:
                        session.delete(record)
                        duplicates_removed += 1

            if duplicates_removed > 0:
                info_id(
                    f"Removed {duplicates_removed} duplicates from {label}",
                    self.request_id,
                )
                self.stats["duplicates_removed"] += duplicates_removed

        except Exception as e:
            error_id(
                f"Error removing duplicates from {label}: {str(e)}",
                self.request_id,
            )
            raise

